class Keycode:
    
    A = "\0\0\04\0\0\0\0\0"
    # a and A

    B = "\0\0\05\0\0\0\0\0"
    # b and B

    C = "\0\0\06\0\0\0\0\0"
    # c and C

    D = "\0\0\07\0\0\0\0\0"
    # d and D

    E = "\0\0\08\0\0\0\0\0"
    # e and E

    F = "\0\0\09\0\0\0\0\0"
    # f and F

    G = "\0\0\0a\0\0\0\0\0"
    # g and G

    H = "\0\0\0b\0\0\0\0\0"
    # h and H

    Pause = "\0\0\48\0\0\0\0\0"
    # Pause key